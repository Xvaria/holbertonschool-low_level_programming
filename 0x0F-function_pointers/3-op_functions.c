#include "3-calc.h"

/**
 * op_add - add
 * @a: first digit
 * @b: second digit
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub
 * @a: first digit
 * @b: second digit
 * Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mul
 * @a: first digit
 * @b: second digit
 * Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - div
 * @a: first digit
 * @b: second digit
 * Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod
 * @a: first digit
 * @b: second digit
 * Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
